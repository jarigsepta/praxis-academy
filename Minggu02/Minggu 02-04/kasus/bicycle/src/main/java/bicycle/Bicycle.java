package bicycle;

public class Bicycle {
    private int cadence;
    private int gear;
    private int speed;

    public void setCadence(int cadence) {
        this.cadence = cadence;
    }

    public int getCadence() {
        return cadence;
    }

    public void setGear(int gear) {
        this.gear = gear;
    }

    public int getGear() {
        return gear;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getSpeed() {
        return speed;
    }

}